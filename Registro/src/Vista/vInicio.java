    private void bAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAccederActionPerformed
        if (!tfUsuario.getText().equals(usuarioFalso)){
        String usuario = tfUsuario.getText();
        char[] contrasena = tfContrasena.getPassword();
        String contrasenaString= String.valueOf(contrasena);
        try {
            vInicioControlador.acceder(usuario,contrasenaString);
            
        } catch (SQLException ex) {
            Logger.getLogger(vInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else{
            vInicioControlador.usuarioFalso();
        }
    }//GEN-LAST:event_bAccederActionPerformed

